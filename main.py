# 1. 데이터 정의 (2차원 리스트)
# ------------------------------------------------------------
supplements = [
    ["비타민B군", 1, "전연령", "공복에 먹으면 속이 쓰릴 수 있으니 식후 복용을 권장합니다."],
    ["밀크씨슬", 1, "20~50대", "알레르기 체질인 경우 성분을 확인하고 복용하세요."],
    ["루테인", 2, "20~50대", "과다 섭취 시 피부가 황색으로 변할 수 있으니 하루 한 알만 드세요."],
    ["오메가3", 2, "전연령", "혈액 응고 억제 작용이 있으므로 수술 전에는 복용을 피하세요."],
    ["유산균", 3, "전연령", "항생제와 함께 복용할 때는 최소 2시간 이상 간격을 두세요."],
    ["칼슘 & 비타민D", 4, "60대 이상", "신장 질환이 있는 경우 복용 전 의사와 상의해야 합니다."]
]

# ------------------------------------------------------------
# 2. 함수 정의
# ------------------------------------------------------------

def display_menu():
    """사용자가 선택할 수 있는 건강 목적 메뉴를 보여준다."""
    print("=" * 40)
    print("   ★ 맞춤형 영양제 추천 프로그램 ★")
    print("=" * 40)
    print("개선하고 싶은 건강 목적의 번호를 선택하세요:")
    print("1. 피로 회복")
    print("2. 눈 건강")
    print("3. 장 건강 / 면역력")
    print("4. 뼈 / 관절 건강")
    print("=" * 40)


def get_user_input():
    """사용자로부터 목적 번호와 연령대를 입력받는다."""
    # input()은 문자로 입력받으므로 숫자로 비교하기 위해 int()로 변환해 줍니다.
    purpose = int(input("원하는 목적의 번호를 입력하세요 (1~4): "))
    age = input("연령대를 입력하세요 (10대, 20~50대, 60대 이상): ")
    return purpose, age


def recommend_supplement(data, purpose, age):
    """2차원 리스트를 탐색하며 조건에 맞는 영양제를 출력한다."""
    print("\n[추천 결과 안내]")
    found = False  # 조건에 맞는 영양제를 찾았는지 확인하는 변수
    
    for row in data:
        # 조건문: 사용자가 입력한 purpose와 age를 리스트의 데이터(row)와 비교합니다.
        if row[1] == purpose and (row[2] == age or row[2] == "전연령"):
            print(f"■ 추천 영양제: {row[0]}")
            print(f"■ 복용 주의사항: {row[3]}")
            print("-" * 40)
            found = True # [해결] 영양제를 찾았으므로 True로 상태를 바꿉니다.

    # 리스트를 다 돌았는데도 하나도 찾지 못했다면 예외 메시지 출력
    if not found:
        print("선택하신 조건에 맞는 추천 영양제가 데이터에 없습니다.")
        print("연령대나 목적 번호를 다시 확인해 주세요.")


def main():
    display_menu() # 1단계: 메뉴 보여주기
    purpose, age = get_user_input() # 2단계: 입력 받기
    recommend_supplement(supplements, purpose, age) # 3단계: 추천 및 결과 출력


# ------------------------------------------------------------
# 3. 프로그램 실행
# ------------------------------------------------------------
main()